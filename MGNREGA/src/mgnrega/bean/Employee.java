package mgnrega.bean;

public class Employee {

	private int adharNo;
	private String name;
	private int age;	
	private int landHoldings;
	private int bankAccount;
	private String bankName;
	private String branchName;
	private String individualAsset;
	private int postNo;
	private String postName;
	private String state;
	
	
	public int getAdharNo() {
		return adharNo;
	}
	public void setAdharNo(int adharNo) {
		this.adharNo = adharNo;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	public int getLandHoldings() {
		return landHoldings;
	}
	public void setLandHoldings(int landHoldings) {
		this.landHoldings = landHoldings;
	}
	public int getBankAccount() {
		return bankAccount;
	}
	public void setBankAccount(int bankAccount) {
		this.bankAccount = bankAccount;
	}
	public String getBankName() {
		return bankName;
	}
	public void setBankName(String bankName) {
		this.bankName = bankName;
	}
	public String getBranchName() {
		return branchName;
	}
	public void setBranchName(String branchName) {
		this.branchName = branchName;
	}
	public String getIndividualAsset() {
		return individualAsset;
	}
	public void setIndividualAsset(String individualAsset) {
		this.individualAsset = individualAsset;
	}
	public int getPostNo() {
		return postNo;
	}
	public void setPostNo(int postNo) {
		this.postNo = postNo;
	}
	public String getPostName() {
		return postName;
	}
	public void setPostName(String postName) {
		this.postName = postName;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	
	
}
